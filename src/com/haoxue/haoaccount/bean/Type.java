package com.haoxue.haoaccount.bean;

/**
 * 说明:总类别
 * 作者:Luoyangs
 * 时间:2015-10-1
 */
public class Type {

	private int id;//ID
	private String name;//名称
	private int state;//状态
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
}
